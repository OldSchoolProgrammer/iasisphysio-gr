function attachEvents(langData) {
  document.querySelectorAll(".lang-switch button").forEach((button) => {
    button.addEventListener("click", () => setLanguage(button.dataset.lang));
  });

  const menuToggle = document.querySelector(".menu-toggle");
  const nav = document.querySelector(".nav");
  if (menuToggle && nav) {
    menuToggle.addEventListener("click", () => {
      nav.classList.toggle("open");
    });
  }

  const form = document.querySelector("#appointment-form");
  if (form) {
    form.addEventListener("submit", async (event) => {
      event.preventDefault();
      const status = form.querySelector(".form-status");
      const submitButton = form.querySelector('button[type="submit"]');
      const turnstileContainer = document.querySelector("#turnstile-container");

      if (!runtimeConfig.contactEnabled) {
        setFormStatus(status, "error", langData.site.formMessages.unavailable);
        return;
      }

      let turnstileToken = "";
      if (runtimeConfig.turnstileSiteKey) {
        if (!window.turnstile || !turnstileContainer?.dataset.widgetId) {
          setFormStatus(status, "error", langData.site.formMessages.captcha);
          return;
        }

        turnstileToken = window.turnstile.getResponse(turnstileContainer.dataset.widgetId);
        if (!turnstileToken) {
          setFormStatus(status, "error", langData.site.formMessages.captcha);
          return;
        }
      }

      const formData = new FormData(form);
      const payload = Object.fromEntries(formData.entries());
      payload.turnstileToken = turnstileToken;

      submitButton.disabled = true;
      setFormStatus(status, "loading", langData.site.formMessages.loading);

      try {
        const response = await fetch("/api/contact", {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
          },
          body: JSON.stringify(payload),
        });

        const result = await response.json().catch(() => ({}));
        if (!response.ok) {
          if (response.status === 503) {
            throw new Error(langData.site.formMessages.unavailable);
          }
          if (response.status === 400 && result.error === "Captcha verification failed") {
            throw new Error(langData.site.formMessages.captcha);
          }
          throw new Error(langData.site.formMessages.error);
        }

        form.reset();
        if (window.turnstile && turnstileContainer?.dataset.widgetId) {
          window.turnstile.reset(turnstileContainer.dataset.widgetId);
        }
        setFormStatus(status, "success", langData.site.formMessages.success);
      } catch (error) {
        setFormStatus(status, "error", error.message || langData.site.formMessages.error);
      } finally {
        submitButton.disabled = false;
      }
    });
  }
}

function setFormStatus(element, kind, message) {
  element.className = `form-status is-${kind}`;
  element.textContent = message;
}

function loadScript(src) {
  return new Promise((resolve, reject) => {
    if (document.querySelector(`script[src="${src}"]`)) {
      resolve();
      return;
    }

    const script = document.createElement("script");
    script.src = src;
    script.async = true;
    script.defer = true;
    script.onload = resolve;
    script.onerror = reject;
    document.head.appendChild(script);
  });
}

async function setupTurnstile() {
  const container = document.querySelector("#turnstile-container");
  if (!container || !runtimeConfig.turnstileSiteKey) {
    return;
  }

  await loadScript("https://challenges.cloudflare.com/turnstile/v0/api.js?render=explicit");
  if (!window.turnstile) {
    return;
  }

  container.innerHTML = "";
  const widgetId = window.turnstile.render("#turnstile-container", {
    sitekey: runtimeConfig.turnstileSiteKey,
    language: document.documentElement.lang === "el" ? "el" : "en",
    theme: "light",
    retry: "auto",
    appearance: "always",
  });
  container.dataset.widgetId = String(widgetId);
}

async function loadRuntimeConfig() {
  try {
    const response = await fetch("/api/config", { headers: { Accept: "application/json" } });
    if (!response.ok) {
      return;
    }

    const result = await response.json();
    runtimeConfig.turnstileSiteKey = typeof result.turnstileSiteKey === "string" ? result.turnstileSiteKey : "";
    runtimeConfig.contactEnabled = Boolean(result.contactEnabled);
  } catch {
    runtimeConfig.turnstileSiteKey = "";
    runtimeConfig.contactEnabled = false;
  }
}

async function renderSite() {
  await loadRuntimeConfig();
  const lang = getSavedLanguage();
  const langData = content[lang];
  const root = document.querySelector("#app");

  document.documentElement.lang = lang;
  document.title = langData.site.titles[pageKey()] || langData.site.titles.home;
  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.setAttribute(
      "content",
      langData.site.descriptions[pageKey()] || langData.site.descriptions.home,
    );
  }

  root.innerHTML = `
    <div class="site-shell">
      ${buildHeader(langData, lang)}
      <main>
        ${renderPage(langData)}
      </main>
      ${buildFooter(langData)}
    </div>
  `;

  attachEvents(langData);
  await setupTurnstile();
}

renderSite();
